if (!function_exists("syriaArtifactsByCity")) {
    function syriaArtifactsByCity(string $city): array
    {
        global $syria_artifacts;
        return array_values(array_filter(
            $syria_artifacts,
            fn($item) => $item["city"] === $city
        ));
    }
}

if (!function_exists("findSyriaArtifact")) {
    function findSyriaArtifact(string $id): ?array
    {
        global $syria_artifacts;
        foreach ($syria_artifacts as $artifact) {
            if ($artifact["id"] === $id) {
                return $artifact;
            }
        }
        return null;
    }
}
