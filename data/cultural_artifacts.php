if (!function_exists("findCulturalArtifact")) {
    function findCulturalArtifact(string $id): ?array
    {
        global $cultural_artifacts;
        foreach ($cultural_artifacts as $artifact) {
            if ($artifact["id"] === $id) {
                return $artifact;
            }
        }
        return null;
    }
}

if (!function_exists("artifactsByCity")) {
    function artifactsByCity(string $city): array
    {
        global $cultural_artifacts;
        return array_values(array_filter(
            $cultural_artifacts,
            fn($item) => $item["city"] === $city
        ));
    }
}
